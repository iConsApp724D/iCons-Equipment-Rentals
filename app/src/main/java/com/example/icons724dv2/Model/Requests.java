package com.example.icons724dv2.Model;

public class Requests {
    private String Name,StudentID, Email, RoomNumber, Item1, Item2, Item3, Item4, Item5;

    public Requests() {
    }

    public Requests(String name, String studentID, String email, String roomNumber, String item1, String item2, String item3, String item4, String item5) {
        Name = name;
        StudentID = studentID;
        Email = email;
        RoomNumber = roomNumber;
        Item1 = item1;
        Item2 = item2;
        Item3 = item3;
        Item4 = item4;
        Item5 = item5;
    }


    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getStudentID() {
        return StudentID;
    }

    public void setStudentID(String studentID) {
        StudentID = studentID;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getRoomNumber() {
        return RoomNumber;
    }

    public void setRoomNumber(String roomNumber) {
        RoomNumber = roomNumber;
    }

    public String getItem1() {
        return Item1;
    }

    public void setItem1(String item1) {
        Item1 = item1;
    }

    public String getItem2() {
        return Item2;
    }

    public void setItem2(String item2) {
        Item2 = item2;
    }

    public String getItem3() {
        return Item3;
    }

    public void setItem3(String item3) {
        Item3 = item3;
    }

    public String getItem4() {
        return Item4;
    }

    public void setItem4(String item4) {
        Item4 = item4;
    }

    public String getItem5() {
        return Item5;
    }

    public void setItem5(String item5) {
        Item5 = item5;
    }
}
